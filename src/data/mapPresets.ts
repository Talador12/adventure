// Map preset terrain templates for quick DM setup.
// Each preset is a 20x20 grid of TerrainType characters.
// Legend: . = floor, # = wall, ~ = water, ^ = difficult, D = door, _ = pit, V = void

import type { TerrainType } from '../lib/mapUtils';

type PresetChar = '.' | '#' | '~' | '^' | 'D' | '_' | 'V';
const CHAR_MAP: Record<PresetChar, TerrainType> = {
  '.': 'floor', '#': 'wall', '~': 'water', '^': 'difficult', 'D': 'door', '_': 'pit', 'V': 'void',
};

function parseGrid(rows: string[]): TerrainType[][] {
  return rows.map((row) => row.split('').map((ch) => CHAR_MAP[ch as PresetChar] || 'floor'));
}

export interface MapPreset {
  id: string;
  name: string;
  description: string;
  icon: string;
  terrain: TerrainType[][];
}

